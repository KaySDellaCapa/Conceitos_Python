"""
É uma técnica utilizada para retornar substrings (partes da string original)
, informando o inicio (start), fim (stop) e passo (step):
[star: stop[, step]].
"""
nome = "Kayque Silva Della Capa"

print(nome[0])

print(nome[:9])

print(nome[10:])

print(nome[10:16])

print(nome[10:16:2]) # Vai do indice 10 a0 16, pegando de 2 em 2

print(nome[:])

print(nome[::-1])
